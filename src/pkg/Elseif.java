//// check whether the number is negative or positive
package pkg;

public class Elseif {

	public static void main(String[] args) {
		int a=20;
		if(a>0) {
			System.out.println("positive");
		}
		else if (a<0)
		{
			System.out.println("negative");
		}
		else {
			System.out.println("number is Zero");
		}

	}

}
